public class ComparisonDemo {

    public static void main(String[] args) {

        //Change value 1 and value 2 and try to guess the conditions which are true

        int value1 = 1;
        int value2 = 1;

        if(value1 == value2)
            System.out.println("value1 == value2");

        if(value1 != value2)
            System.out.println("value1 != value2");

        if(value1 > value2)
            System.out.println("value1 > value2");

        if(value1 < value2)
            System.out.println("value1 < value2");

        if(value1 <= value2)
            System.out.println("value1 <= value2");
        
    }
    
}
